          <strong>The Kentucky Center for the Performing Arts – Louisville, KY – Lobby  Lighting Renovation</strong>

          <img class="right-img img-responsive" src="images/KCA1.jpg" />

          <br>
          <br>

          <p>After  renovating all three performance spaces to full Sensor+ AF Dimming, ETCNet3  networking, and Paradigm architectural and Eos Family console control only  three years ago, Cross light was called upon again to assist with the  renovation of the lighting in the 130 foot long main lobby.  New York lighting consultants Focus Lighting  designed a new scheme for the lobby, replacing the old metal halide flood  lights with new Color Kinetics RGB LED fixtures.  The once green-white cylindrical ceiling is  now the canvas for multi-colored light shows and projected imagery.  Cross Light worked again with the site and  electrical contractors Ready Electric to provide a unified network control  system with ETC <u>Mosaic</u> and Unison <u>Paradigm</u>, speaking not only  ETCNet3, but also Color Kinetics’ KiNET directly to the LED fixtures and  Art-Net to the two High End DL3<u> </u>moving projectors.  Cross Light <u>APG</u> programmer Jonathan  Fuchs lead the nightly programming at the consultants’ direction, creating  dozens of various looks from subtle to high-impact.   This system also integrates a handful of new  and existing conventional dimmer racks and relay contactors to bring the  general lighting to the same point of control.</p>
