          <img class="right-img img-responsive" src="images/Centrum - 5373526246_2f978ebb5d_b.jpg" />

          <br>
          <br>

          <p>Electrical Contractor Cassady Electric called upon Cross Light to assist the owners in updating the building’s lighting systems during a major rehab and renovation. The domed ceiling of the former Sanctuary now incorporates ETC Selador Vivid fixtures to bathe the structure in anything from a warm glow, to soft pastels, to bright saturated colors.  Energy efficient quartz downlight fixtures provide lighting for any performance events that take place.  The 2nd space, the Auditorium, is now fully functional as a small performance space with a full electrics package of connector strips, Source Four PAR and Source Four Jr. fixtures.  The peripheral meeting rooms incorporate local architectural control over fluorescent and incandescent lighting provided by the building-wide system controlled by ETC Unison Paradigm networked processors and Sensor+ Dimmer Racks.  LCD touchscreens allow flexibility over the spaces, from color-picker screens, to combining breakout meeting spaces in the Auditorium. Localized button and fader stations provide simple control under day-to-day operations.  The lighting system integrates with other trades, controling shades in the auditorium, and overriding lighting circuits in the Sanctuary when the projection screen is lowered.  In addition, the main tower outside incorporates RGB floodlights run via an ETC Unison Mosaic show controller that plays colored light shows according to automatic holiday events, or a simple “candlelight” look on most days. </p>

