          <strong>Traditional Dimming – And Rigging Too! </strong>

          <img class="right-img img-responsive" src="images/muhlenburg.jpg"  height="190" width="287" alt="Wright State University" />

          <br>
          <br>

          <p>Incandescent dimming still has a place in the world.  Nowhere is that more clear than theatrical performing arts spaces like Muhlenberg High School in Greenville, KY or the new Eastern Kentucky University performing arts center in Richmond, KY.  Both sites recently installed new Sensor Dimming, Paradigm architectural control, and Eos-family consoles.  New to Cross Light is the addition of the ETC Prodigy line of motorized hoists.  Muhlenberg incorporates a full system of house light hoists, stage electric hoists, and general purpose batten hoists, all from a single point of control.  Also in Louisville, Valley High School and Western Middle School include front-of-house lighting hoists to compliment their ETC dimming systems, and improve access to theatrical fixtures without the need for a catwalk.  Valley High School’s new auditorium also includes the Prodigy Houselight hoists (pictured) to ease fixture re-lamping. Western high school added theatrical front lighting where none existed in the renovation of a decades-old room. </p>

          <br>
          <br>

          <img class="left-img img-responsive" src="images/WrightUniversity.jpg" alt="Muhlenburg High School" width="290"  height="164" />

          <strong>“Control, control, you must learn control!” - <em>Yoda </em></strong><br />

          <br>
          <br>

          <p> Sometimes a unique control situation arises where no solution exists yet.  At the Wright State University Creative Arts Center, theatrical consultants Jones and Philips designed a series of motorized acoustical drapes that hide behind acoustically transparent walls which allow for the dynamic modification of the sound properties of the room utilizing DMX-controlled DeSisti hoists.  Cross Light provided a unique preset-control solution in the form of a Mosaic Tessera touchscreen DMX controller.  From there, each individual banner can be proportionately positioned to adjust the tuning of the space.  All of the banners retracted allow the natural live sound for a vocal performance; fully deployed, they absorb the sound in the small space for a large orchestra; to everything in between. </p>

