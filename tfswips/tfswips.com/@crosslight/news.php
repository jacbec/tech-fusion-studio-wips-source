          <strong>Paramount Theatre – Anderson, IN</strong>

          <br>
          <br>

          <p>The Paramount Theater in Anderson, Indiana experienced an incredibly unusual event.  A minor earthquake on December 30 broke a water main and caused water to pour into the main electrical distribution room under the theater.  As this room filled with water over the course of a four day holiday, the main distribution panel became submerged and began to boil the flood water.  Steam vented through the conduits that feed directly into the dimmer racks, melting the electronics within.  After an emergency call from West Electric on January 3, Cross Light inspected the damaged equipment on January 4 and determined a replacement system would be required.  Through an order placed with Indianapolis Stage on January 6, new equipment arrived onsite January 10.  Installed by January 13, main power was restored to the building through new switchgear, and Cross Light commissioned the new dimmer racks.  New button and Touchscreen stations were programmed the morning of the 14th, prior to performers taking the stage at 4:00PM that evening for their performance at 7:00PM.  Thanks to amazing support from ETC, and everybody involved working together so closely, the theatre was back up and running in a very short period of time.</p>

