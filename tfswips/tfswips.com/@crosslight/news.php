          <strong>Northeast Christian Church - Louisville, KY</strong>

          <br>
          <br>

          <p>LED fixtures are not just for effect anymore.  When Northeast Christian Church in Louisville was unhappy with the dimming capabilities of their fluorescent houselight fixtures, they looked to warm-white Affineon LED Downlights.  To verify the light levels and coverage that the site desired, Cross Light utilized AGI32 calculations to provide point-by-point footcandle calculations from the Affineon IES files.  With that, the site was able to use half as many fixtures in existing locations to minimize new wiring, installation time, and power consumption.  The addition of DMX control allows full range 0-100% dimming and the ability to balance each fixture individually throughout the various heights in the room.  </p>

