          <p>The renovation of the dimming and control systems for each of the three venues in the <a href="http://www.kentuckycenter.org/" rel="external">Kentucky Center for the Performing Arts</a> was completed in a tight two-month time frame this summer.  Working closely with the end user, the manufacturer ETC, the dealer/integrator <a href="http://www.onlocationls.com" rel="external">On Location Lighting Systems</a> and the local electrical contractor <a href="http://www.readyelec.com/" rel="external">Ready Electric</a>, Cross Light helped renovate three spaces to full ETC dimming, network, console and fixtures.  We provided drawing assistance to the owner and electrical engineer <a href="http://www.kerr-greulich.com/" rel="external">Kerr-Greulich</a>, as well as technical expertise with regard to specific hardware.  In the end, twenty-two existing dimmer racks were replaced with new Sensor+ AF racks, DMX distribution upgraded to ETCNet3 Gateways, and theatrical control replaced with ETC EOS, ION, and Element consoles.  The architectural control was also replaced with the new Unison Paradigm, to bring the entire facility under one manufacturer for the first time since its original construction.</p>

