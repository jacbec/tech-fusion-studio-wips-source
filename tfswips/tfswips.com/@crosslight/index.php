          <p>When Horseshoe Casino opened in 2013, Cross Light was there to support the new, 400,000 square foot facility.  The main casino floor is powered by 16 ETC Sensor3 Dimmer Racks, and is controlled by a sprawling, fiber-connected network of 4 Unison Paradigm processors with countless stations for local control in the clubs, restaurants, and partition-able ballroom.  Emergency lighting is also controlled through the dimmer racks, via UL924 emergency lighting bypass.</p>

          <br>

          <p>Cross Light worked with local  contractors and distributors for the sale and installation of the system,  providing on-site assistance as needed thanks to our proximity to the  jobsite.  Sunman to downtown Cincinnati  is only about 40 minutes’ drive.  As time  approached for the opening, Cross Light field service technicians Jim Sjogren  and Jonathan Fuchs worked to commission the system, bringing the network,  dimming, and control equipment on-line. Jonathan worked with the Lighting consultants to program a control  scheme that was flexible yet intuitive for the owner.  Throughout the process, Cross Light worked  with the electricians to maintain accurate records of the thousand or so  lighting loads controlled by the system. </p>

          <br>

          <p>In addition to the Casino itself, ETC dimming  controls the LED and Fluorescent lighting on the Event Lawn, sure to draw  concert crowds this summer.  This system  ties to the main building system for override control if necessary.<p/>

